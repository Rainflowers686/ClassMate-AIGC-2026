    val included: List<OfficialCapability> = all.filterNot { it.excluded }
    val productFacing: List<OfficialCapability> = all.filter { it.productFacing }
    val smokeOnly: List<OfficialCapability> = all.filter { it.tier == OfficialCapabilityTier.DEV_LAB_SMOKE }
    val excluded: List<OfficialCapability> = all.filter { it.excluded }
    val allSafeSmoke: List<OfficialCapability> = all.filter { it.includedInAllSafeSmoke }

    fun byId(id: String): OfficialCapability? =
        all.firstOrNull { it.id.equals(id, ignoreCase = true) }

    fun userSurfaceCapabilities(): List<OfficialCapability> =
        productFacing.filterNot { it.excluded }

