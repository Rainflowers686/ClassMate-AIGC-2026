package com.classmate.core.official

enum class OfficialCapabilityTier {
    PRODUCT_FACING,
    DEV_LAB_SMOKE,
    EXCLUDED,
}

enum class OfficialCapabilityStatus {
    PRODUCT_READY,
    INTEGRATED,
    SEAM_READY,
    SMOKE_ONLY,
    EXCLUDED,
    CONFIG_MISSING,
}

data class OfficialCapability(
    val id: String,
    val docId: Int,
    val displayName: String,
    val officialTitle: String,
    val tier: OfficialCapabilityTier,
    val status: OfficialCapabilityStatus,
    val cloudPath: String,
    val onDeviceFallback: String,
    val manualFallback: String,
    val userVisibleLabel: String,
    val developerNotes: String,
    val safeSmokeAllowed: Boolean,
    val includedInAllSafeSmoke: Boolean = false,
) {
    val productFacing: Boolean get() = tier == OfficialCapabilityTier.PRODUCT_FACING
    val excluded: Boolean get() = tier == OfficialCapabilityTier.EXCLUDED
}

