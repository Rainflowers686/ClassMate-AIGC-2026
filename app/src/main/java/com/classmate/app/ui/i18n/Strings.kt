import java.util.Locale

/**
 * Lightweight in-app language system. Default is Chinese; English is a full toggle; "follow system"
 * resolves against the device locale at read time. Kept as plain Kotlin (no Android resources) so
 * the language switch is instant (no Activity recreation) and the string packs are unit-testable.
 *
 * Technical short codes (BlueLM, LocalFallback, qwen3.5-plus, JSON, Debug) stay as-is in
 * both languages on purpose.
 */
enum class AppLanguage(val displayName: String) {
    ZH("中文"),
    EN("English"),
    SYSTEM("跟随系统");

    /** Resolve to a concrete pack (ZH or EN); SYSTEM follows the device locale. */
    fun resolve(): AppLanguage = when (this) {
        SYSTEM -> if (Locale.getDefault().language.equals("zh", ignoreCase = true)) ZH else EN
        else -> this
    }

    fun displayNameFor(current: AppLanguage): String = when (this) {
        SYSTEM -> if (current.resolve() == EN) "System" else "跟随系统"
        else -> displayName
    }
}

