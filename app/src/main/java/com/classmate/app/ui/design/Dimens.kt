package com.classmate.app.ui.design

import androidx.compose.ui.unit.dp

/** Spacing & sizing tokens — one scale so screens stay visually consistent. */
object Dimens {
    val xxs = 4.dp
    val xs = 6.dp
    val s = 8.dp
    val m = 12.dp
    val l = 16.dp
    val xl = 20.dp
    val xxl = 28.dp
    val xxxl = 40.dp

    /** Default horizontal padding for screen content. */
    val screen = 20.dp

    /** Inner padding for cards. */
    val cardPadding = 18.dp

    /** Vertical gap between stacked cards. */
    val cardGap = 14.dp
}
